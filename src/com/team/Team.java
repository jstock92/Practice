package com.team;

public class Team {
	
	String sport;
	int numPlayers;
	String city;
	int salaryCap;
	String logo;
	
	public Team (String sport, int numPlayers, String city, int salaryCap, String logo){
		
		this.sport = sport;
		this.numPlayers = numPlayers;
		this.city = city;
		this.salaryCap = salaryCap;
		this.logo = logo;
		
		
	}
	
	public Team (){
		
		this.sport = sport;
		this.numPlayers = numPlayers;
		this.city = city;
		this.salaryCap = salaryCap;
		this.logo = logo;
		
		
	}

}
